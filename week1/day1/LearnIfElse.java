package week1.day1;

public class LearnIfElse {

	public static void main(String[] args) {

		int num=5;
		if(num>6) {
			System.out.println("it is positive");
		}else {
			System.out.println("Number is not greater than 6");
		}
		
		
		int age =19;
		if(age>25) {
			System.out.println("he is working person");
		}else if(age>19) {
			System.out.println("he is not working person");
		}else {
			System.out.println("No data found");
		}
	}

}
